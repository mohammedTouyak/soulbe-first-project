// Function to translate the "Our History" section
function translateOurHistory(language) {
    const section = document.getElementById("our-history");
    const translation = translations[language]["our-history"];

    if (!section || !translation) return; // Vérifier si la section ou les traductions existent

    // Traduire le titre et le sous-titre
    const title = section.querySelector("h2");
    if (title) {
        title.innerHTML = `${translation.title1}<br><small>${translation.subtitle1}</small>`;
    }

    // Traduire les paragraphes
    const paragraphs = section.querySelectorAll("p");
    if (paragraphs.length > 0) {
        paragraphs[0].textContent = translation.content1 || "";
        paragraphs[1].textContent = translation.content2 || "";
    }
}

// Function to translate the "Feedback" section
function translateFeedback(language) {
    const section = document.getElementById("reviews");
    const translation = translations[language].reviews;

    if (!section || !translation) return; // Vérifier si la section ou les traductions existent

    // Traduire le titre et le sous-titre
    const title = section.querySelector("h2");
    if (title) {
        title.innerHTML = `${translation.title}<br><small>${translation.subtitle}</small>`;
    }

    // Traduire les cartes d'avis
    const reviewCards = section.querySelectorAll(".review-card");
    reviewCards.forEach(card => {
        const reviewTitle = card.querySelector("h3");
        const reviewBody = card.querySelector("p");
        const reviewerInfo = card.querySelector(".reviewer-info");

        if (reviewTitle) reviewTitle.textContent = translation.reviewTitle || "";
        if (reviewBody) reviewBody.textContent = translation.reviewBody || "";
        if (reviewerInfo) {
            const spans = reviewerInfo.querySelectorAll("span");
            if (spans[0]) spans[0].textContent = translation.reviewerName || "";
            if (spans[1]) spans[1].textContent = translation.reviewDate || "";
        }
    });

    // Traduire la description générale
    const description = document.getElementById("reviews-description-section");;
    if (description) {
        description.querySelector("p").textContent = translation.description || "";
    }
}

// Function to translate the "Find Us" section
function translateFindUs(language) {
    const sectionText = document.getElementById("form-container");
    const contactUs = document.getElementById("contact-us");
    const translation = translations[language].findUs;

    if (!contactUs || !sectionText || !translation) return;

    // Translate the title and paragraphs in "form-container"
    const title = sectionText.querySelector("h2");
    if (title) {
        title.innerHTML = `${translation.title}<br><small>${translation.subtitle}</small>`;
    }

    const paragraphs = sectionText.querySelectorAll("p");
    if (paragraphs.length > 0) {
        paragraphs[0].textContent = translation.content1;
        paragraphs[1].textContent = translation.content2;
    }

    // Translate the form in "contact-us"
    if (contactUs) {
        const formTitle = contactUs.querySelector("h3");
        if (formTitle) formTitle.textContent = translation.contactUsTitle;

        const formLabels = contactUs.querySelectorAll("label");
        const inputs = contactUs.querySelectorAll("input, textarea");
        const submitButton = contactUs.querySelector(".submit-btn");

        if (formLabels.length > 0) {
            formLabels[0].textContent = translation.formLabels.name;
            formLabels[1].textContent = translation.formLabels.surname;
            formLabels[2].textContent = translation.formLabels.email;
            formLabels[3].textContent = translation.formLabels.message;
        }

        if (inputs.length > 0) {
            inputs[0].placeholder = translation.formLabels.placeholderName;
            inputs[1].placeholder = translation.formLabels.placeholderSurname;
            inputs[2].placeholder = translation.formLabels.placeholderEmail;
            inputs[3].placeholder = translation.formLabels.placeholderMessage;
        }

        if (submitButton) {
            submitButton.textContent = translation.formLabels.submit;
        }
    }
}

// Function to translate the navigation bar
function translateNav(language) {
    const navItems = document.querySelectorAll(".navbar-nav .nav-item");
    const translation = translations[language].nav;

    if (!translation) return;

    const languages = document.querySelector(".languages");
    languages.querySelector("small").textContent = translation['language'];

    navItems.forEach((item, index) => {
        const link = item.querySelector(".nav-link");
        if (!link) return;

        // Determine which item to translate based on index
        let key;
        switch (index) {
            case 0:
                key = "home";
                break;
            case 1:
                key = "whoWeAre";
                break;
            case 2:
                key = "ourServices";
                break;
            case 3:
                key = "photos";
                break;
            case 4:
                key = "contactUs";
                break;
        }

        if (translation[key]) {
            link.innerHTML = `${translation[key].title}<br><small>${translation[key].subtitle}</small>`;
        }
    });
}

// Function to translate the footer
function translateFooter(language) {
    const footer = document.querySelector(".footer");
    const translation = translations[language].footer;

    if (!translation) return;

    // Translate "Our Resources" section
    const resourcesSection = footer.querySelector(".footer-section:nth-of-type(2)");
    if (resourcesSection) {
        const resourcesTitle = resourcesSection.querySelector("h4");
        if (resourcesTitle) resourcesTitle.textContent = translation.resourcesTitle;

        const resourcesList = resourcesSection.querySelectorAll("ul li a");
        if (resourcesList.length > 0) {
            resourcesList.forEach((item, index) => {
                item.textContent = translation.resources[index] || "";
            });
        }
    }

    // Translate "External Links" section
    const linksSection = footer.querySelector(".footer-section:nth-of-type(3)");
    if (linksSection) {
        const linksTitle = linksSection.querySelector("h4");
        if (linksTitle) linksTitle.textContent = translation.linksTitle;

        const linksList = linksSection.querySelectorAll("ul li a");
        if (linksList.length > 0) {
            linksList.forEach((item, index) => {
                item.textContent = translation.links[index] || "";
            });
        }
    }

    // Translate footer-bottom
    const footerBottom = footer.querySelector(".footer-bottom");
    if (footerBottom) {
        footerBottom.textContent = translation.footerBottom;
    }
}

